// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(x) {
  let str = '';
  for (let i = 1; i <= x; i++) {
    if (i % 2 === 0) { // even # rows
      for (let i = 1; i <= x; i++) {
        if (i % 2 === 0) { // even # columns
          str += ' ';
        } else {            // odd # columns
          str += '#'

        } if (i === x) {
          str += '\n';
        }
      }
      
    } else {        //odd # rows
      for (let i = 1; i <= x; i++) {
        if (i % 2 === 0) { // even # columns
          str += '#';
        } else {            // odd # columns
          str += ' '

        } if (i === x) {
          str += '\n';
      }
    }
  }

} console.log(str);
}
