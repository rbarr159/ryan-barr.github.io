////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(a, b) {
if (a > b) {
  return b;
} else {
  return a;
}
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(a) {
return a % 2 === 0;
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, x) {
  let ctr = 0;
  for (let i = 0; i < str.length; i++) {
    if (str[i] === x) {
      ctr +=1;
    }
  } return ctr;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str) {
      let ctr = 0;
    for (let i = 0; i < str.length; i++) {
      if (str[i].toUpperCase() === 'B') {
        ctr +=1;
      }
    } return ctr;

}

